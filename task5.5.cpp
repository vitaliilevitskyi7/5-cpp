#include <iostream>

int main() {
    // Початкові умови: x1 = x2 = x3 = -99
    long long x1 = -99;
    long long x2 = -99;
    long long x3 = -99;

    // Оскільки перші три члени від'ємні, починаємо розрахунок з n = 4
    int n = 4;
    long long x_curr = 0;

    while (true) {
        // Формула: x_n = x_{n-1} + x_{n-3} + 100
        // Для n=4: x4 = x3 + x1 + 100
        x_curr = x3 + x1 + 100;

        // Якщо знайшли додатне число, перериваємо цикл
        if (x_curr > 0) {
            break;
        }

        // Зсув змінних для наступного кроку
        x1 = x2;
        x2 = x3;
        x3 = x_curr;
        n++;
    }

    std::cout << "Найменший додатний член послідовності: " << x_curr << "\n";
    std::cout << "Його порядковий номер (індекс n): " << n << "\n";

    return 0;
}
